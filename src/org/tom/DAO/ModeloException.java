package org.tom.DAO;

public class ModeloException extends Exception {

	public ModeloException() {
		super();
	}

	public ModeloException(String arg0) {
		super(arg0);
	}

	public ModeloException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ModeloException(Throwable arg0) {
		super(arg0);
	}
}